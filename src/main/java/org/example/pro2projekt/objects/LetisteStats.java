package org.example.pro2projekt.objects;

public class LetisteStats {
    private String Stat;
    private int Pocet;
    private int Kapacita;
    private int AllLetiste;

    public LetisteStats() {

    }

    public String getStat() {
        return Stat;
    }

    public void setStat(String stat) {
        Stat = stat;
    }

    public int getPocet() {
        return Pocet;
    }

    public void setPocet(int pocet) {
        Pocet = pocet;
    }

    public int getKapacita() {
        return Kapacita;
    }

    public void setKapacita(int kapacita) {
        Kapacita = kapacita;
    }

    public int getAllLetiste() {
        return AllLetiste;
    }

    public void setAllLetiste(int allLetiste) {
        AllLetiste = allLetiste;
    }
}
